<template>
  <div id="app">
    <div id="nav">
      <router-link to="/">Home</router-link> |
      <router-link to="/about">About</router-link>
    </div>
    <div>{{ $t('btn.changeLanguage') }}</div>
    <hr>
    <button @click="changeLanguage">{{$t('btn.changeLanguage')}}</button>
    <router-view/>
  </div>
</template>

<script>
export default {
  methods: {
    changeLanguage() {
      if (this.$i18n.locale === 'zh') {
        this.$i18n.locale = 'en'
      } else {
        this.$i18n.locale = 'zh'
      }
    }
  }
}
</script>


